void solve() {
	string a, b;
	cin >> a;
	cin >> b;
	int dp[a.size() + 1][b.size() + 1];

	int lcs = 0;
	for (int i = 0; i <= a.size(); i++) {
		for (int j = 0; j <= b.size(); j++) {
			if (i == 0 || j == 0) {
				dp[i][j] = 0;
				continue;
			}

			dp[i][j] = (a[i - 1] == b[j - 1] ? 1 + dp[i - 1][j - 1] : 0);
			lcs = max(lcs, dp[i][j]);
		}
	}

	cout << a.size() + b.size() - 2 * lcs; NL
	return;
}






